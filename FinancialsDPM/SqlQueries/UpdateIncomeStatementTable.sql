            [incomerBeforeTax] = @_incomerBeforeTax,
            [incomeTaxExpense] = @_incomeTaxExpense,
            [interestAndDebtExpense] = @_interestAndDebtExpense,
            [netIncomeFromContinuingOperations] = @_netIncomeFromContinuingOperations,
            [comprehensiveIncomeNetOfTax] = @_comprehensiveIncomeNetOfTax,
            [ebit] = @_ebit,
            [ebitda] = @_ebitda,
            [netIncome] = @_netIncome


        WHERE [Ticker] = @_Ticker
    END
ELSE
    BEGIN
        Insert into [dbo].[IncomeStatementDataTable](  [Ticker],
