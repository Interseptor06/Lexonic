        UPDATE [dbo].[IncomeStatementDataTable]
        SET [fiscalDateEnding] = @_fiscalDateEnding,
            [reportedCurrency]  = @_reportedCurrency,
            [grossProfit] = @_grossProfit,
            [totalRevenue] = @_totalRevenue,
            [costOfRevenue] = @_costOfRevenue,
            [costofGoodsAndServicesSold] = @_costofGoodsAndServicesSold,
            [operatingIncome] = @_operatingIncome,
            [sellingGeneralAndAdministrative] = @_sellingGeneralAndAdministrative,
